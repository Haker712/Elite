package bean.classes;

public class DailyChecking {
	
String dcheckNo, shopname, distributor, checkingtime, state;

public String getDcheckNo() {
	return dcheckNo;
}

public void setDcheckNo(String dcheckNo) {
	this.dcheckNo = dcheckNo;
}

public String getShopname() {
	return shopname;
}

public void setShopname(String shopname) {
	this.shopname = shopname;
}

public String getDistributor() {
	return distributor;
}

public void setDistributor(String distributor) {
	this.distributor = distributor;
}

public String getCheckingtime() {
	return checkingtime;
}

public void setCheckingtime(String checkingtime) {
	this.checkingtime = checkingtime;
}

public String getState() {
	return state;
}

public void setState(String state) {
	this.state = state;
}

}
