package bean.classes;

public class SlideProduct
{
	String productName, bringQty, delivreQty, remainQty;

	public String getProductName() {
		return productName;
	}

	public void setProductName(String productName) {
		this.productName = productName;
	}

	public String getBringQty() {
		return bringQty;
	}

	public void setBringQty(String bringQty) {
		this.bringQty = bringQty;
	}

	public String getDelivreQty() {
		return delivreQty;
	}

	public void setDelivreQty(String delivreQty) {
		this.delivreQty = delivreQty;
	}

	public String getRemainQty() {
		return remainQty;
	}

	public void setRemainQty(String remainQty) {
		this.remainQty = remainQty;
	}

}
